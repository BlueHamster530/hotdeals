# 고정 분류체계 — AI 분류가 이 중 하나(또는 '기타')만 반환하도록 제약. 필터 일관성 유지.
CATEGORIES: list[str] = [c for c, _ in _CATEGORY_KEYWORDS] + ["기타"]


def parse_price(title: str) -> int | None:
    """제목에서 가격(원)을 추출. 괄호 안 가격을 우선, 없으면 가장 큰 4자리+ 숫자."""
    # 1) 괄호 안 가격 우선 (보통 실제 판매가가 괄호에 들어감)
    for bracket in _BRACKETS_RE.findall(title):
        m = _PRICE_RE.search(bracket)
        if m:
            return int(m.group(1).replace(",", ""))
    # 2) 전체에서 후보 중 최댓값 (배송비 같은 작은 수 회피)
    candidates = [int(x.replace(",", "")) for x in _PRICE_RE.findall(title)]
    candidates = [c for c in candidates if c >= 1000]  # 1000원 미만은 가격으로 보지 않음
    return max(candidates) if candidates else None


def guess_category(title: str) -> str | None:
    low = title.lower()
    for category, keywords in _CATEGORY_KEYWORDS:
        if any(kw in low for kw in keywords):
            return category
    return None


def normalized_key(title: str) -> str:
    """같은 상품을 묶기 위한 매칭 키. 쇼핑몰 태그·괄호·가격·기호 제거 후 소문자화.

    완벽하지 않다(예: 용량/색상 차이를 못 가름). 동일 상품 그룹핑의 v1 휴리스틱이며,
    PriceHistory 정확도가 중요해지면 임베딩 유사도로 보강한다.
    """
    t = _SHOP_TAG_RE.sub("", title)
    t = _BRACKETS_RE.sub(" ", t)
    t = t.lower()
    t = _NON_KEY_RE.sub("", t)
    return t[:255]
